// Un operador ternario en JavaScript es una forma abreviada de
// escribir una estructura condicional if...else. Se llama
// "ternario" porque utiliza tres partes:


// condición ? valor_si_verdadero : valor_si_falso
// Ejemplo básico:

let edad = 18;
let mensaje = edad >= 18 ? "Eres mayor de edad" : "Eres menor de edad";
console.log(mensaje);